/**
 * An enum representing the possible positions of the towers in the Tower of
 * Hanoi puzzle.
 * 
 * @author Tarini Duvvuri
 * @version 10.17.23
 */
public enum Position {

    /**
     * Represents the left tower.
     */
    LEFT,

    /**
     * Represents the middle tower.
     */
    MIDDLE,

    /**
     * Represents the right tower.
     */
    RIGHT,

    /**
     * Represents a default or undefined position.
     */
    DEFAULT;
}
